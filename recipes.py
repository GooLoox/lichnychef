def find_recipe(query: str) -> dict | None:
    """Ищет рецепт по запросу (нечёткий поиск)."""
    query = query.lower().strip()
    
    # Точное совпадение
    if query in RECIPES:
        return {"name": query, **RECIPES[query]}
    
    # Частичное совпадение
    for name, recipe in RECIPES.items():
        if query in name or name in query:
            return {"name": name, **recipe}
    
    # Поиск по словам
    query_words = query.split()
    for name, recipe in RECIPES.items():
        if any(word in name for word in query_words if len(word) > 3):
            return {"name": name, **recipe}
    
    return None

def get_recipe_list(category: str = None) -> list:
    """Возвращает список рецептов по категории."""
    if category:
        return [name for name, r in RECIPES.items() if r["category"] == category]
    return list(RECIPES.keys())

def format_recipe_for_prompt(recipe: dict, portions: int = 1) -> str:
    """Форматирует рецепт для передачи в GPT-промт."""
    lines = [f"Рецепт: {recipe['name']} на {portions} чел.\n"]
    lines.append("Ингредиенты:")
    
    for item, amount in recipe["per_person"].items():
        # Умножаем числовое значение на количество порций
        amount_str = _multiply_amount(amount, portions)
        lines.append(f"  • {item}: {amount_str}")
    
    if recipe.get("time"):
        lines.append(f"\nВремя приготовления: {recipe['time']}")
    if recipe.get("tip"):
        lines.append(f"Совет: {recipe['tip']}")
    
    return "\n".join(lines)

def _multiply_amount(amount_str: str, portions: int) -> str:
    """Умножает количество на число порций."""
    import re
    match = re.match(r"([\d.]+)(.*)", amount_str)
    if match:
        num = float(match.group(1)) * portions
        unit = match.group(2)
        if num == int(num):
            return f"{int(num)}{unit}"
        return f"{num:.1f}{unit}"
    return amount_str

